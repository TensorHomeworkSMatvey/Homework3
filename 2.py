print('Пример ввода: "4 влево"; Стоп-слово: "Стоп"')
x, y, a = 0, 0, input("Введите куда вы хотите двигаться:").split()
if a[0] == "Стоп": print("Программа закончила выполнение"), exit()
while a[0] != "Стоп":
    if a[1] == "влево": x -= float(a[0])
    elif a[1] == "вправо": x += float(a[0])
    elif a[1] == "вверх": y += float(a[0])
    elif a[1] == "вниз": y -= float(a[0])
    print(f'Новые координаты: ({x}, {y})')
    a = input("Введите куда вы хотите двигаться:").split()
print("Программа закончила выполнение")